	private Block placeBuildingBlock(Random random) {
        if (random.nextDouble() < 0.005) {
            return BlocksAether.fuse_trap;
        } else if (random.nextDouble() >= 0.005 && random.nextDouble() < 0.01) {
        	return BlocksAether.fuse_trap_2;
        } else if (random.nextDouble() >= 0.01 && random.nextDouble() < 0.05) {
        	return BlocksAether.locked_creeping_stone;
        } else {
            return BlocksAether.locked_fuse_stone;
        }
    }
